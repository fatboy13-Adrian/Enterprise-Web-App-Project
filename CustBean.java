package myjsp;

public class CustBean
{
	public String giftName;
	public int    purchases;
	public int    price;
	public int    quantity;
	
	public CustBean(){}
	
	public String getGiftName()
	{
		return giftName;
	}
	
	public int getPurchases()
	{
		return purchases;
	}
	
	public int getPrice()
	{
		return purchases;
	}
	
	public int getQuantity()
	{
		return quantity;
	}
	
	public void setGiftName(String x)
	{
		giftName = x;
	}
	
	public void setPurchases(int x)
	{
		purchases = x;
	}
	
	public void setPrice(int x)
	{
		price = x;
	}
	
	public void setQuantity(int x)
	{
		quantity = x;
	}
}