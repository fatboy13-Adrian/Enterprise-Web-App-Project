package myjsp;

public class WareHouseBean
{
	public String giftName;
	public int    quantity;
	
	public WareHouseBean(){}
	
	public String getGiftName()
	{
		return giftName;
	}
	
	public int getQuantity()
	{
		return quantity;
	}
	
	public void setGiftName(String x)
	{
		giftName = x;
	}
	
	public void setQuantity(int x)
	{
		quantity = x;
	}
}