package myjsp;

public class SalesBean
{
	public String giftName;
	public int    price;
	
	public SalesBean(){}
	
	public String getGiftName()
	{
		return giftName;
	}
	
	public int getPrice()
	{
		return price;
	}
	
	public void setGiftName(String x)
	{
		giftName = x;
	}
	
	public void setPrice(int x)
	{
		price = x;
	}
}